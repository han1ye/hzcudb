package cn.ljh.db.model;

public class BeanMatchs {
	private String matchsCode;
	private String matchsName;
	private String organizer;
	private String contractor;
	private boolean matchsGroup;
	private String matchsInfo;
	public String getMatchsCode() {
		return matchsCode;
	}
	public void setMatchsCode(String matchsCode) {
		this.matchsCode = matchsCode;
	}
	public String getMatchsName() {
		return matchsName;
	}
	public void setMatchsName(String matchsName) {
		this.matchsName = matchsName;
	}
	public String getOrganizer() {
		return organizer;
	}
	public void setOrganizer(String organizer) {
		this.organizer = organizer;
	}
	public String getContractor() {
		return contractor;
	}
	public void setContractor(String contractor) {
		this.contractor = contractor;
	}
	public boolean isMatchsGroup() {
		return matchsGroup;
	}
	public void setMatchsGroup(boolean matchsGroup) {
		this.matchsGroup = matchsGroup;
	}
	public String getMatchsInfo() {
		return matchsInfo;
	}
	public void setMatchsInfo(String matchsInfo) {
		this.matchsInfo = matchsInfo;
	}
	
	

}
