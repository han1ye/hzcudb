package cn.ljh.db.model;

public class BeanTeacher {
	private String teaNum;
	private String teaName;
	private String belongCollege;
	private String teaTel;
	private String teaemail;
	public String getTeaNum() {
		return teaNum;
	}
	public void setTeaNum(String teaNum) {
		this.teaNum = teaNum;
	}
	public String getTeaName() {
		return teaName;
	}
	public void setTeaName(String teaName) {
		this.teaName = teaName;
	}
	public String getBelongCollege() {
		return belongCollege;
	}
	public void setBelongCollege(String belongCollege) {
		this.belongCollege = belongCollege;
	}
	public String getTeaTel() {
		return teaTel;
	}
	public void setTeaTel(String teaTel) {
		this.teaTel = teaTel;
	}
	public String getTeaemail() {
		return teaemail;
	}
	public void setTeaemail(String teaemail) {
		this.teaemail = teaemail;
	}
	
	

}
